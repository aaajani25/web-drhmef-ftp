  <p>Une administration économique et financière dynamique et performante avec un personnel de qualité et compétent dans ses différents domaines d’intervention.</p>
      <p>Telle est la vision qu’exprimait Monsieur le Ministre, dans son discours d’ouverture du séminaire d’orientation stratégique organisé au mois d’août 2016 à Grand-Bassam, en vue de définir les grands axes prioritaires qui vont lui servir d’appui pour mener à bien son action. </p>
      <p>A cet effet, dans le cadre de la mise en œuvre du Plan National de Développement 2016-2020</p>
      <p>Cinq axes prioritaires ont été définis : </p>
      <ul>
        <li>
          <p>mobilisation et optimisation des ressources pour le financement de l’économie&nbsp;;</p>
        </li>
        <li>
          <p>soutien à la transformation structurelle de l’économie&nbsp;;</p>
        </li>
        <li>
          <p>modernisation de l’Administration Economique et Financière ;</p>
        </li>
        <li>
          <p>conduite des réformes économiques, financières et monétaires&nbsp;;</p>
        </li>
        <li>
          <p>amélioration de la gouvernance et promotion de l’éthique.</p>
        </li>
      </ul>
      <p>La mise en œuvre de ces axes, nécessite un personnel qualifié et compétent dans ses différents domaines d’intervention, d’où la nécessité de renforcer régulièrement ses capacités.</p>
      <p>Dans cette perspective, la mise en place d’une politique de formation apparaît comme un impératif, qui s’inscrit dans l’axe «&nbsp;Modernisation de l’Administration Economique et Financière&nbsp;».</p>
      <br>
      <p>Il revient à la DRH, dont le rôle statutaire en matière de formation a été réaffirmé par les résolutions du séminaire
        d'orientation stratégique tenu au mois d'août 2016 à Grand-Bassam, d’élaborer et de mettre en œuvre d’une part, la politique de formation et d’autre part,
        le plan de formation du ministère. Ces deux actions sont inscrites dans le Plan d’Actions Opérationnelles 2017. </p>
      <br>
      <p>Toutefois, pour des raisons pragmatiques et surtout, du fait que les unités administratives du Ministère mettaient déjà en œuvre leurs plans de formation sectoriels et qu’une opportunité de financement par le Programme Pays de Renforcement des Capacités (PPRC) s’offrait à elle, la DRH a procédé, en premier lieu, à l’élaboration du plan de formation qui devrait se faire logiquement après celle de la politique de formation du Ministère. </p>
      <br>
      <p>Pour ce faire, il lui a paru stratégique de coordonner, dans un premier temps, les actions et besoins de formation sectoriels. C’est ce qui a été fait et le Comité technique qui a élaboré ce plan de formation a été reconduit en 2018 en vue de &nbsp;l’élaboration de la politique de formation du Ministère.</p>
      <br>
      <p>Depuis lors, avec les orientations de la tutelle, le processus a repris son cours normal. Il n’y a donc pas de dichotomie entre le plan de formation 2017-2020 dont la mise en œuvre est effective depuis le mois de janvier 2018 et la présente politique de formation qui le soutient. </p>
      <br>
      <p>Cette politique de formation, pilotée par la DRH, doit être comprise comme un élément de la politique d’ensemble du ministère visant à assurer de manière durable sa rentabilité, la satisfaction de ses clients, l’implication de son personnel et une relation positive avec son environnement.</p>
      <p>Elle exprime une volonté affichée par le top management, et engageant toute l’entité et portant sur les axes essentiels qui vont orienter les décisions et les actes de gestion de la formation et, par extension, de gestion des compétences.</p>
