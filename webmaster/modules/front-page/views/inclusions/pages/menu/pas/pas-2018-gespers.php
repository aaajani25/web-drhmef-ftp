      <p>
Le déploiement de l’outil Gespers dans les autres structures du MEF a pour but de permettre l’utilisation rationnelle, en vue d’une gestion efficace, des ressources humaines du Ministère.
Il s’agit donc d’installer ce logiciel dans les autres structures du MEF ne disposant pas d’outil informatique de gestion des Ressources Humaines. Pour celles qui en disposent, il s’agit de créer une interface en vue des échanges d’informations entre le logiciel sectoriel de GRH et l’outil Gespers de la DRH/MEF.<br>

Le déploiement actuel de Gespers dans les autres structures du Ministère se fait en deux étapes distinctes que sont :
       </p>

       <ul>
           <li>La mise en place du lien physique informatique entre la structure concernée et la DRH ;</li>
            <li>L’installation et la configuration de l’application Gespers.</li>
 </ul>

        <span><strong>2.1 L’installation et la configuration de l’application Gespers.</strong></span>
  <p>
La première étape du déploiement de Gespers est dévolue au Service Réseaux et Maintenance de la Sous-Direction de l’Informatique.
Cette étape consiste, essentiellement, à rendre fonctionnel, le lien physique informatique, si ce dernier existe entre la structure concernée par le déploiement de Gespers et la Direction des Ressources Humaines. Dans le cas contraire, tout est mis en œuvre pour mettre en place ce lien physique.
</p>

