        <p>
        Au regard du décret n°2016-600 du 03 août 2016 portant organisation du Ministère auprès du Premier Ministre , chargé de l’Economie et des Finances, la DRH a pour missions essentielles, la coordination de la formation et la gestion des Ressources Humaines du Ministère.
A ce titre, elle doit disposer, en temps réel, des effectifs consolidés du Ministère en relation avec les organes en charge de la gestion sectorielle des ressources humaines dans les autres structures du Ministère.
Cela suppose d'elle, la mise en place d'un système d'information capable de fédérer l'ensemble des outils sectoriels de gestion des ressources humaines utilisés dans les autres structures du Ministère. Un tel dispositif nécessite une étude diagnostic du Système d’Information du (SI) de la DRH afin de prendre en compte toutes les exigences en la matière.
L'objectif fondamental de l'étude diagnostic du Système d'Information (SI) de la DRH est de mettre en place, à moyen terme, un système intégré des outils de gestion des ressources humaines pour l'ensemble du Ministère tel que recommandé par le PAO 2018 du Ministère de l'Economie et des Finances (MEF).
Cette étude diagnostic du Système d'Information (SI) de la DRH concerne les structures du Ministère ayant, en leur sein, un organe de gestion des ressources humaines. La cible est donc constituée de la DRH et de ses ramifications dans les autres structures du Ministère.

