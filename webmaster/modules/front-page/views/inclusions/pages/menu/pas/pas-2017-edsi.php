  Cela suppose d’elle, la mise en place d’un système d’information capable de fédérer l’ensemble des outils sectoriels de gestion des ressources humaines utilisés dans les autres structures du Ministère. Un tel dispositif nécessite qu’elle fasse sa mue dans une certaine mesure et ensuite,
