          Au regard du décret n°2016-600 du 03 août 2016 portant organisation du Ministère auprès du Premier Ministre , chargé de l’Economie et des Finances, la DRH a pour missions essentielles, la coordination de la formation et la gestion des ressources Humaines du Ministère.
