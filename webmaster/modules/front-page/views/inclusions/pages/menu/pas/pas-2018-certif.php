La Direction des Ressources Humaines du Ministère de l’Economie et des Finances (DRH/MEF) a été créée conformément au décret n°2011-290
du 12 octobre 2011 portant institution d’un poste de Directeur des Ressources Humaines dans tous les Ministères.<br>
Hiérarchiquement,elle est rattachée au Cabinet du Ministre et constitue son interface avec le
Ministère de la Fonction Publique en matière de gestion administrative des fonctionnaires et agents de l’Etat du MEF.
Pour mener à bien sa mission, elle a décidé de mettre en place un Système de Management de la Qualité (SMQ) opérationnel selon la norme
ISO 9001 version 2015, en vue de la certification.
Faisant de ce projet la clé de voûte de sa gouvernance, le Directeur des Ressources Humaines, M. BAMBA Lancina, s’est engagé à investir toutes les bonnes idées et les ressources nécessaires pour sa mise en œuvre cohérente et déterminée.
Pour en assurer le bon fonctionnement, il a délégué au Chef du Service Qualité et Contrôle Interne, la responsabilité de la supervision, du reporting et de la gestion des relations internes et externes.
<br >En voulant traduire samotivation principale à travers la qualité des prestations et de service ainsi qu’une image de marque valorisée, il a pris le pari d’une vision axée sur une DRH moderne et performante.
Les gains d’une telle démarche sont nombreux notamment,l’amélioration du niveau de satisfaction des clients, la motivation du personnel, la réduction du taux de gaspillage, la reconnaissance internationale, l’amélioration de l'image de la DRH.<br>
Ainsi,tous les grands projets de modernisation et les innovations sont orientés en vue de propulser cette Direction au cœur des avantages et de la satisfaction dont peuvent bénéficier autant les clients internes qu’externes.
Il est de notoriété que la problématique de la qualité du service public est une réalité en Côte d’Ivoire. C’est pourquoi la DRH-MEF a conçu son SMQ pour s’améliorer.
Et, plus que le référentiel ISO choisi,continuellement en cause qui constitue l’ancrage de ce projet de modernisation.
