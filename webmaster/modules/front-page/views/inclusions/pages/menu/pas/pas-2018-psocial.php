<div class="page_art">
  <div class="panel-grid title">
        <h3 class="widget-title">CONTEXTE ET JUSTIFICATION</h3>
    </div>
  <div class="page_art_content">
	<p>Dans le cadre de l&rsquo;exécution du programme de  gouvernement, dont la mission principale s&rsquo;articule autour de l&rsquo;accession de la  Côte d&rsquo;Ivoire au rang des pays émergeants à l&rsquo;horizon 2020, plusieurs mesures  ont été adoptées notamment dans le développement du secteur économique et  social.
	</p>
  <p>Dans cette dynamique, le Ministère de l’Economie et des Finances(MEF) a défini son Plan d’Actions Stratégiques 2017-2020 autour de cinq (05) axes à savoir :
	<ul>
	  <li>mobiliser et optimiser les ressources pour le financement de l’économie </li>
	  <li>soutenir les transformations structurelles de l’économie </li>
    <li>moderniser l’Administration Economique et Financière </li>
    <li>conduire les réformes économiques, financières et monétaires</li>
    <li>améliorer la gouvernance et promouvoir l’éthique</li>
	</ul>
  </p>
  <p>Cette entreprise vise la transformation structurelle du Ministère ainsi que l’amélioration des conditions de travail et du bien-être social des agents.</p>
  <p>Cependant, force est de constater que malgré les efforts déjà consentis en matière d’actions sociales, le ministère ne dispose pas d’une politique sociale formelle et uniforme pour l’ensemble de ses structures.</p>
  <p>En vue de synchroniser toutes les actions sociales existantes et suite aux recommandations du séminaire sur les orientations stratégiques du Ministère, tenu à Grand-Bassam les 10 et 12 aout 2016,  la  Direction des Ressources Humaines (DRH/MEF) est chargée de l’élaboration de la politique sociale dudit Ministère.</p>
  <p>Pour la réalisation de cette mission la DRH/MEF bénéficiera du concours de l’ensemble des structures du Ministère dans un cadre de coordination des réalités et besoins sectoriels en vue d’aboutir à un Document de Base de Politique Sociale du MEF, DCBPS/MEF.</p>
</div>
<div class="panel-grid title">
        <h3 class="widget-title">OBJECTIFS</h3>
    </div>
    <div class="page_art_content">
<p>Ce projet vise à consolider les actions sociales sectorielles, en renforçant les activités contribuant au bien-être social de tous les agents du Ministère de l’Economie et des Finances, par l’instauration d’une politique sociale formelle et uniforme.</p>
<p>Il s&rsquo;agit  de&nbsp;:
<ul>
  <li>faire l’état des lieux du dispositif social des structures du MEF</li>
  <li>élaborer la politique sociale</li>
  <li>élaborer  la charte de la politique sociale</li>
  <li>vulgariser la politique sociale </li>
  <li>mettre en œuvre la politique sociale</li>
</ul>
</p>
</div>
<div class="panel-grid title">
        <h3 class="widget-title">RESULTATS ATTENDUS</h3>
    </div>
    <div class="page_art_content">
<p>Les résultats attendus sont ci-après définis&nbsp;:
<ul>
  <li>enquêtes  réalisées</li>
  <li>politique sociale élaborée</li>
  <li>charte  de politique sociale élaborée</li>
  <li>plan de sensibilisation et de communication élaboré</li>
  <li>politique sociale mise en œuvre</li>
  <li>plan de suivi et évaluation disponible</li>
</ul>
</p>
</div>
<div class="panel-grid title">
        <h3 class="widget-title">PERIMETRE D'APPLICATIONS</h3>
    </div>
    <div class="page_art_content">
<p>Ce projet concerne l’ensemble des agents du MEF.</p>
</div>
<div class="panel-grid title">
        <h3 class="widget-title">METHODOLOGIE</h3>
    </div>
    <div class="page_art_content">
<p>Le projet  devra s&rsquo;articuler autour des étapes suivantes&nbsp;: </p>
<p><strong>Phase 1&nbsp;: Mise en place du Comité Technique</strong>
<ul>
  <li>Adresser un courrier à toutes les  structures du Ministère pour la désignation des points focaux </li>
  <li>Mettre en place le Comité Technique et le Secrétariat Technique </li>
  <li>Elaborer un projet de texte  portant création du Comité Technique<strong> </strong></li>
  <li>Elaborer un plan d&rsquo;actions <strong> </strong></li>
</ul>
</p>
<p>
  <strong>Phase 2&nbsp;: </strong><strong>Etat des lieux du dispositif social des structures du MEF</strong>
<ul>
  <li>Recueillir la vision de la hiérarchie</li>
  <li>Recueillir les avis des partenaires sociaux</li>
  <li>Identifier les actions sociales existantes des structures du MEF</li>
  <li>Définir les domaines d'intervention</li>
</ul>
</p>
<p>
<strong>Phase 3: Elaboration de la politique sociale du MEF </strong>
  <ul>
  <li>Rédiger les Termes de Référence de la politique sociale</li>
  <li>Elaborer un projet de politique sociale</li>
  <li>Elaborer la charte de politique sociale</li>
  <li>Pré valider le projet de politique sociale en comité</li>
  <li>Soumettre le projet à la hiérarchie</li>
  <li>Organiser un atelier de validation de la politique sociale</li>
</ul>
</p>
<p>
<strong>Phase 4: </strong><strong>Vulgarisation  de la politique sociale&nbsp;;</strong>
  <ul>
  <li>Faire un plan de sensibilisation et de communication de la politique sociale</li>
  <li>Mettre en œuvre le plan de sensibilisation et de communication</li>
  <li>Diffuser le document de politique sociale</li>
</ul>
</p>
<p>
<strong>Phase 5: Suivi et évaluation </strong>
<ul>
  <li>Exécuter la charte de politique sociale</li>
  <li>Evaluer la mise en œuvre de la politique</li>
  <li>Rédiger les rapports d'activités et de suivi-évaluation</li>
</ul>
</p>
</div>
