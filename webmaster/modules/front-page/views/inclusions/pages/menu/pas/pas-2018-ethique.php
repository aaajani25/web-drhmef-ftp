<p  id="docs-internal-guid-1d6e533e-7fff-3ec7-a564-26999fb1910a">Dans le cadre de la mise en œuvre du PAO 2018, un atelier a été organisé par la DRH-MEF le 26 décembre 2017 en vue de procéder à la validation des projets de Code de Déontologie et de Charte d&rsquo;Ethique du Ministère de l&rsquo;Economie et des Finances.<br>
La validation de ces documents de façon consensuelle par les représentants de toutes les composantes de notre département ministériel, ont permis l&rsquo;élaboration de versions définitives soumises à l&rsquo;appréciation de Monsieur le Ministre de l&rsquo;Economie et des Finances.</p>
<p>Ainsi, pour cette année 2018, la DRH-MEF a pour objectif de définir son positionnement dans le processus de promotion des valeurs éthiques et déontologiques et de la bonne gouvernance.<br>
L&rsquo;action de la DRH-MEF consiste donc en :
<ul>
  <li>
la réception et le traitement des plaintes en cas de violation constatée des principes et valeurs éthiques et déontologiques ;
</li>
<li>l&rsquo;amplification de la portée des informations relatives aux autres structures du MEF ;</li>
<li>la coordination des actions de formation et de sensibilisation aux valeurs éthiques et règles de déontologie.</li>
</ul>
<p> Le 21 novembre 2018, Monsieur le Ministre de l&rsquo;Economie et des Finances a bien voulu signer deux (02) arrêtés :</p>
<ul>
  <li >
    <p>l&rsquo;arrêté n°329/MEF du 21 novembre 2018 portant création du Comité d&rsquo;éthique et de déontologie du Ministère de l&rsquo;Economie et des Finances ;</p>
  </li>
  <li >
    <p>l&rsquo;arrêté n°330/MEF du 21 novembre 2018 instituant un code de déontologie au Ministère de l&rsquo;Economie et des Finances. </p>
  </li>
</ul>
<p>Ces textes permettront à la Direction des Ressources Humaines de déployer effectivement durant l&rsquo;année 2019, le plan de sensibilisation et de formation aux principes et valeurs éthiques et déontologiques. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p>Par ailleurs, elle entend nouer des partenariats avec les différentes entités administratives du Ministère et les structures extérieures en charge des questions d&rsquo;éthique, de déontologie et de bonne gouvernance.</p>
