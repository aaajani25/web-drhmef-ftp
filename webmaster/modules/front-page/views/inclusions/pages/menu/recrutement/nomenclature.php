      <p>Le Gouvernement dans son ensemble est sensible à la   question du chômage des jeunes. Cette épineuse question ne trouve   malheureusement pas sa solution uniquement dans les recrutements à la   Fonction Publique. A ce titre, des programmes de développement de   ´emploi des jeunes sont initiés par le Gouvernement et pris en charge   par des structures techniques telles que l´AGEPE.<br />
        S´agissant des Concours Administratifs d´entrée à la Fonction Publique, l´âge limite réglementaire est de 40   ans. Cependant, compte tenu de l´effectif pléthorique actuel des   fonctionnaires, l'Etat recrutera désormais à partir des <strong>besoins réels exprimés par les Administrations Techniques</strong>.<br />
