<p style="color:#090909; text-align:justify">
  L’éthique et la déontologie sont deux notions que les organisations intègrent de plus en plus dans leur système de management. De plus, loin d’être une singularité de notre Ministère, la mise en place de dispositifs de promotion des valeurs éthiques et règles de déontologie est recommandée par l’ordonnance n°2013-660 du 20 septembre 2013 portant prévention et lutte contre la corruption et les infractions assimilées.
Cette ordonnance recommande l’élaboration de règles et codes de conduite des agents publics.&nbsp;De par leur mission commune, les agents du Ministère de l’Economie et des Finances peuvent être considérés comme faisant partie d’une corporation, dont l’activité professionnelle peut être régie par un référentiel unique.

Ainsi, l’élaboration d’un code de déontologie et d’une charte d’éthique du Ministère de l’Economie et des Finances répond à la volonté du premier responsable de ce département,&nbsp;<strong>M. Adama KONE</strong> , de doter les agents de documents de référence visant à : <br>

- garantir la transparence dans la gestion des finances publiques ;<br>
- apporter une solution aux récriminations des usagers/clients à l’endroit de l’administration économique et financière ; <br>

- contribuer à l’avènement de l’ « ivoirien nouveau », un citoyen avec un comportement exemplaire, de surcroît lorsqu’il a une mission de gestion des deniers publics.
</p>

